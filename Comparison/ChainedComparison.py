# lehetséges a logikai operátorok összefűzése a következőképp:

print(1 < 2 < 3)                            # True

# ez szöveges operátorral a következő:

print(1 < 2 and 2 < 3)                      # True


print(1 < 3 > 2)                            # True  -  ez pedig gyakorlatilag egyenlő ezzel: 1 < 3 and 3 > 2
